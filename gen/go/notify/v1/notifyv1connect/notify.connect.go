// elloloop/notify — Connect/gRPC service contract.
//
// Two services live here:
//
//   NotificationInternalService — backend-to-backend, gRPC. Producers (any
//     consumer service, e.g. a todo / messaging / billing backend) call
//     Notify to fan a payload out to one-or-many users across one-or-many
//     delivery channels. The platform stores per-user rows idempotently and
//     attempts immediate delivery wherever a provider + destination is
//     available.
//
//   NotificationClientService — client-facing (browser / mobile), Connect
//     HTTP/2. Recipients stream live events, page their inbox, ack/read
